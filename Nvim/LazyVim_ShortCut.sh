$(Space cm) # Open Plugin Manager (LSP)
$(Space e)  # Open & Close File Manager
$(Ctrl /)   # Open & Close Terminal
$(Space fp) # Change Project
$(Space fb) # List All Buffers

$(Space s)  # +Search
$(Space uC) # Change Themes
$(Space sk) # Search KeyMap

$(]d)       # Next Error
$(Space ca) # Fix Error

$(Ctrl h j k l)            # Jump Split Windows
$(Ctrl Up Down Left Right) # Resize Split Windows
